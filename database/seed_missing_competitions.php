// ===================================================================
// MAIN EXECUTION
// ===================================================================

try {
    $db->setAttribute(PDO::ATTR_ERRMODE, PDO::ERRMODE_EXCEPTION);
    $db->exec("SET NAMES utf8mb4 COLLATE utf8mb4_unicode_ci");

    $totalCreated = 0;

    // --- Получить ID специализаций по slug ---
    $specStmt = $db->prepare("SELECT id FROM audience_specializations WHERE slug = ? LIMIT 1");

    function getSpecId($pdo, $slug) {
        static $cache = [];
        if (isset($cache[$slug])) return $cache[$slug];
        $stmt = $pdo->prepare("SELECT id FROM audience_specializations WHERE slug = ? LIMIT 1");
        $stmt->execute([$slug]);
        $row = $stmt->fetch(PDO::FETCH_ASSOC);
        $cache[$slug] = $row ? (int)$row['id'] : null;
        return $cache[$slug];
    }

    // --- Проверить, есть ли конкурсы для (audience_type_id, specialization_id) ---
    function hasCompetitions($pdo, $atId, $specId) {
        $stmt = $pdo->prepare(
            "SELECT COUNT(*) FROM competition_audience_types cat
             JOIN competition_specializations cs ON cat.competition_id = cs.competition_id
             WHERE cat.audience_type_id = ? AND cs.specialization_id = ?"
        );
        $stmt->execute([$atId, $specId]);
        return (int)$stmt->fetchColumn() > 0;
    }

    // --- Проверить, есть ли конкурсы для audience_type_id (без специализации) ---
    function hasCompetitionsForType($pdo, $atId) {
        $stmt = $pdo->prepare(
            "SELECT COUNT(*) FROM competition_audience_types WHERE audience_type_id = ?"
        );
        $stmt->execute([$atId]);
        return (int)$stmt->fetchColumn() > 0;
    }

    // =============================================
    // Обработка групп со специализациями
    // =============================================
    foreach ($allTemplates as $groupKey => $group) {
        // G. Дошкольники (без специализаций)
        if (!empty($group['no_spec'])) {
            $atId = $group['audience_type_id'];
            $catId = $group['audience_category_id'];

            if (!hasCompetitionsForType($db, $atId)) {
                echo "<h3>Дошкольники (type {$atId}): создаю 3 конкурса</h3>\n";
                foreach ($group['competitions'] as $comp) {
                    $id = insertCompetition($db, $comp, $catId, [$atId], null);
                    echo "<p>  + #{$id}: {$comp['title']}</p>\n";
                    $totalCreated++;
                }
            } else {
                echo "<p>✓ Дошкольники — уже есть конкурсы</p>\n";
            }
            continue;
        }

        // B. Ролевые специализации
        if ($groupKey === 'roles') {
            $catId = $group['audience_category_id'];
            foreach ($group['specs'] as $specSlug => $comps) {
                $specId = getSpecId($db, $specSlug);
                if (!$specId) {
                    echo "<p style='color:orange'>⚠ Специализация '{$specSlug}' не найдена</p>\n";
                    continue;
                }

                $typeIds = $roleTypeMap[$specSlug] ?? [];

                // Проверяем, есть ли конкурсы хоть для одного типа
                $needCreate = true;
                foreach ($typeIds as $tId) {
                    if (hasCompetitions($db, $tId, $specId)) {
                        $needCreate = false;
                        break;
                    }
                }

                if ($needCreate) {
                    echo "<h3>Роль: {$specSlug} (типы: " . implode(',', $typeIds) . "): создаю 3 конкурса</h3>\n";
                    foreach ($comps as $comp) {
                        $id = insertCompetition($db, $comp, $catId, $typeIds, $specId);
                        echo "<p>  + #{$id}: {$comp['title']}</p>\n";
                        $totalCreated++;
                    }
                } else {
                    echo "<p>✓ {$specSlug} — уже есть конкурсы</p>\n";
                }
            }
            continue;
        }

        // A, C, D, E, F: Группы со специализациями и одним типом
        $atId = $group['audience_type_id'];
        $catId = $group['audience_category_id'];

        foreach ($group['specs'] as $specSlug => $comps) {
            $specId = getSpecId($db, $specSlug);
            if (!$specId) {
                echo "<p style='color:orange'>⚠ Специализация '{$specSlug}' не найдена</p>\n";
                continue;
            }

            if (!hasCompetitions($db, $atId, $specId)) {
                echo "<h3>{$groupKey} / {$specSlug} (type {$atId}): создаю 3 конкурса</h3>\n";
                foreach ($comps as $comp) {
                    $id = insertCompetition($db, $comp, $catId, [$atId], $specId);
                    echo "<p>  + #{$id}: {$comp['title']}</p>\n";
                    $totalCreated++;
                }
            } else {
                echo "<p>✓ {$groupKey}/{$specSlug} — уже есть конкурсы</p>\n";
            }
        }
    }

    echo "<h2 style='color:green'>✓ Готово! Создано конкурсов: {$totalCreated}</h2>\n";
