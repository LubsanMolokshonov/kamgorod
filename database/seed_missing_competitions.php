// Транслитерация (из Competition::generateSlug)
function transliterate($title) {
    $slug = mb_strtolower($title, 'UTF-8');
    $map = [
        'а' => 'a', 'б' => 'b', 'в' => 'v', 'г' => 'g', 'д' => 'd',
        'е' => 'e', 'ё' => 'yo', 'ж' => 'zh', 'з' => 'z', 'и' => 'i',
        'й' => 'y', 'к' => 'k', 'л' => 'l', 'м' => 'm', 'н' => 'n',
        'о' => 'o', 'п' => 'p', 'р' => 'r', 'с' => 's', 'т' => 't',
        'у' => 'u', 'ф' => 'f', 'х' => 'h', 'ц' => 'ts', 'ч' => 'ch',
        'ш' => 'sh', 'щ' => 'sch', 'ъ' => '', 'ы' => 'y', 'ь' => '',
        'э' => 'e', 'ю' => 'yu', 'я' => 'ya'
    ];
    $slug = strtr($slug, $map);
    $slug = preg_replace('/[^a-z0-9]+/', '-', $slug);
    return trim($slug, '-');
}

function uniqueSlug($pdo, $slug) {
    $original = $slug;
    $counter = 1;
    while (true) {
        $stmt = $pdo->prepare("SELECT id FROM competitions WHERE slug = ?");
        $stmt->execute([$slug]);
        if (!$stmt->fetch()) return $slug;
        $slug = $original . '-' . $counter++;
    }
}

function insertCompetition($pdo, $data, $categoryId, $audienceTypeIds, $specId = null) {
    $slug = uniqueSlug($pdo, transliterate($data['title']));

    $nominations = is_array($data['nomination_options'])
        ? json_encode($data['nomination_options'], JSON_UNESCAPED_UNICODE)
        : $data['nomination_options'];

    $stmt = $pdo->prepare("INSERT INTO competitions
        (title, slug, description, target_participants, target_participants_genitive,
         award_structure, academic_year, category, nomination_options, price, is_active, display_order)
        VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, 1, 0)");

    $stmt->execute([
        $data['title'], $slug, $data['description'],
        $data['target_participants'], $data['target_participants_genitive'],
        'Диплом I, II, III степени в электронном виде',
        '2025-2026', $data['category'], $nominations, $data['price'] ?? 149
    ]);

    $compId = $pdo->lastInsertId();

    // competition_audience_categories
    $stmt = $pdo->prepare("INSERT IGNORE INTO competition_audience_categories (competition_id, category_id) VALUES (?, ?)");
    $stmt->execute([$compId, $categoryId]);

    // competition_audience_types
    $stmt = $pdo->prepare("INSERT IGNORE INTO competition_audience_types (competition_id, audience_type_id) VALUES (?, ?)");
    foreach ($audienceTypeIds as $atId) {
        $stmt->execute([$compId, $atId]);
    }

    // competition_specializations
    if ($specId) {
        $stmt = $pdo->prepare("INSERT IGNORE INTO competition_specializations (competition_id, specialization_id) VALUES (?, ?)");
        $stmt->execute([$compId, $specId]);
    }

    return $compId;
}

