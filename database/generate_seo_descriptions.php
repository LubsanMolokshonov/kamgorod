function generateUniqueSeoDescription($comp, $intros, $benefits, $ctas) {
    $title = $comp['title'];
    $target = $comp['target_participants'];
    $targetGen = $comp['target_participants_genitive'] ?: $target;
    $category = $comp['category'];
    $description = $comp['description'];
    $price = number_format($comp['price'], 0, ',', ' ');
    $id = $comp['id'];

    // Выбираем элементы на основе id для уникальности
    $intro = $intros[$category][$id % count($intros[$category])];

    // Формируем 3 абзаца

    // 1. Вступление + что это
    $p1 = "{$intro} «{$title}»! {$description} Конкурс проводится в дистанционном формате на протяжении всего учебного года и открыт для {$targetGen} из всех регионов Российской Федерации.";

    // 2. Преимущества (выбираем 4 уникальных)
    $selectedBenefits = [];
    $benefitCount = count($benefits);
    for ($i = 0; $i < 4; $i++) {
        $selectedBenefits[] = $benefits[($id * 3 + $i) % $benefitCount];
    }
    $p2 = "Преимущества участия:\n• " . implode("\n• ", $selectedBenefits) . "\n\nСтоимость участия — всего {$price} рублей. Оплата производится онлайн, результаты и диплом предоставляются в электронном виде.";

    // 3. Процедура + CTA
    $cta = $ctas[$id % count($ctas)];
    $p3 = "Для участия достаточно зарегистрироваться на сайте, выбрать номинацию и загрузить работу. Конкурсные материалы оцениваются по критериям: актуальность, оригинальность, полнота раскрытия темы, практическая значимость, качество оформления и соответствие требованиям ФГОС. {$cta}";

    return "{$p1}\n\n{$p2}\n\n{$p3}";
}

function generateUniqueGoals($comp, $goalTemplates) {
    $category = $comp['category'];
    $title = $comp['title'];
    $targetGen = $comp['target_participants_genitive'] ?: $comp['target_participants'];
    $id = $comp['id'];

    $templates = $goalTemplates[$category] ?? $goalTemplates['methodology'];

    // Берём 4 цели, варьируя набор на основе id
    $goals = [];
    $count = count($templates);
    for ($i = 0; $i < 4; $i++) {
        $tpl = $templates[($id + $i) % $count];
        $tpl = str_replace('%title%', $title, $tpl);
        $tpl = str_replace('%target_genitive%', $targetGen, $tpl);
        $goals[] = $tpl;
    }

    return implode("\n", $goals);
}

function generateUniqueObjectives($comp, $objectiveTemplates) {
    $category = $comp['category'];
    $id = $comp['id'];

    $templates = $objectiveTemplates[$category] ?? $objectiveTemplates['methodology'];

    // Берём 5 задач, варьируя набор на основе id
    $objectives = [];
    $count = count($templates);
    for ($i = 0; $i < 5; $i++) {
        $objectives[] = $templates[($id + $i) % $count];
    }
    $objectives[] = "Наградить победителей и участников дипломами I, II, III степени в электронном формате";

    return implode("\n", $objectives);
}

