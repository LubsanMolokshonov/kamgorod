try {
    $db->setAttribute(PDO::ATTR_ERRMODE, PDO::ERRMODE_EXCEPTION);

    // Получаем конкурсы без SEO-описания
    $stmt = $db->query(
        "SELECT * FROM competitions
         WHERE is_active = 1
         AND (seo_description IS NULL OR seo_description = '')
         ORDER BY id"
    );
    $competitions = $stmt->fetchAll(PDO::FETCH_ASSOC);

    echo "<p>Найдено конкурсов без SEO-описания: <b>" . count($competitions) . "</b></p>\n";

    if (empty($competitions)) {
        echo "<p style='color:green'>✓ Все конкурсы уже имеют SEO-описания</p>\n";
        exit;
    }

    $updateStmt = $db->prepare(
        "UPDATE competitions SET goals = ?, objectives = ?, seo_description = ? WHERE id = ?"
    );

    $success = 0;
    foreach ($competitions as $comp) {
        $seoDesc = generateUniqueSeoDescription($comp, $intros, $benefits, $ctas);
        $goals = generateUniqueGoals($comp, $goalTemplates);
        $objectives = generateUniqueObjectives($comp, $objectiveTemplates);

        $updateStmt->execute([$goals, $objectives, $seoDesc, $comp['id']]);
        $success++;

        if ($success <= 5) {
            echo "<details><summary>#{$comp['id']}: {$comp['title']}</summary>\n";
            echo "<pre>" . htmlspecialchars(mb_substr($seoDesc, 0, 300)) . "...</pre></details>\n";
        }
    }

    echo "<h2 style='color:green'>✓ Обновлено конкурсов: {$success}</h2>\n";
    echo "<p><a href='/konkursy'>Проверить на сайте →</a></p>\n";

} catch (Exception $e) {
    echo "<p style='color:red'>Ошибка: " . htmlspecialchars($e->getMessage()) . "</p>\n";
    echo "<pre>" . htmlspecialchars($e->getTraceAsString()) . "</pre>\n";
}
