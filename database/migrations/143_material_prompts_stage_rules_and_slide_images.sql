-- Миграция 143: правила под ступень (ДОО/1 класс/ОВЗ) + иллюстрации слайдов + УМК.
-- Дата: 2026-06-08
--
-- Причина: «Тест 2» методистов показал, что промпты миграции 134 (школоцентричные) дают
-- для детского сада школьную модель урока (учитель/ученик, УУД, домашние задания, баллы) —
-- оценки «1». Плюс презентации генерировались без иллюстраций, а часть материалов — без УМК.
--
-- Что делает миграция (всё идемпотентно, через NOT LIKE-гарды):
--   1) Вставляет в начало КАЖДОГО шаблона новый плейсхолдер {stage_rules} — жёсткие правила под
--      ступень/особенности (подставляются MaterialType::renderPrompt() / stageRules()):
--        • ДОО — воспитатель/дети, игра, наглядность, без ДЗ/баллов/УУД, целевые ориентиры;
--        • 1 класс — безотметочное обучение (Приказ № 115);
--        • ОВЗ/СДВГ из «особенностей группы» — конкретные адаптации.
--   2) Добавляет в промпт презентации требование per-slide image_prompt и «3–5 коротких буллетов»
--      (иллюстрации рисует MaterialGenerator::attachSlideImages() через YandexART).
--   3) Просит указывать УМК в рабочем листе и презентации (типовой по программе, если не задан).

SET NAMES utf8mb4 COLLATE utf8mb4_unicode_ci;

-- 1) Плейсхолдер {stage_rules} в начало всех шаблонов (один раз).
UPDATE material_types
   SET ai_prompt_template = CONCAT('{stage_rules}\n\n', ai_prompt_template)
 WHERE ai_prompt_template IS NOT NULL
   AND ai_prompt_template <> ''
   AND ai_prompt_template NOT LIKE '%{stage_rules}%';

-- 2) Презентация: иллюстрации и короткие буллеты.
UPDATE material_types
   SET ai_prompt_template = CONCAT(
       ai_prompt_template,
       '\n\nИЛЛЮСТРАЦИИ И ОБЪЁМ СЛАЙДА (ОБЯЗАТЕЛЬНО): каждый содержательный слайд — 3–5 КОРОТКИХ ',
       'буллетов-тезисов, без сплошного текста и без переноса всей лекции на слайд. Для каждого ',
       'содержательного слайда добавь в JSON слайда поле "image_prompt" — короткое описание ',
       'иллюстрации на русском языке (предметная, реалистичная, БЕЗ текста и подписей на картинке). ',
       'Для титульного и итогового слайдов image_prompt можно не заполнять.'
   )
 WHERE slug = 'prezentatsiya'
   AND ai_prompt_template NOT LIKE '%image_prompt%';

-- 3) УМК в рабочем листе и презентации (типовой по программе, если в задании не указан).
UPDATE material_types
   SET ai_prompt_template = CONCAT(
       ai_prompt_template,
       '\n\nУМК: укажи в JSON поле "umk" — учебник/УМК, к которому привязан материал. Если УМК не ',
       'задан в особенностях — подбери типовой по программе/предмету/классу (напр. «Школа России») ',
       'и привяжи задания к нему.'
   )
 WHERE slug IN ('rabochiy-list', 'prezentatsiya')
   AND ai_prompt_template NOT LIKE '%"umk"%';
