<script>
(function() {
    var lb = document.getElementById('imageLightbox');
    if (!lb) return;

    var overlay = lb.querySelector('.image-lightbox__overlay');
    var img = lb.querySelector('.image-lightbox__img');
    var counter = lb.querySelector('.image-lightbox__counter');
    var prevBtn = lb.querySelector('.image-lightbox__prev');
    var nextBtn = lb.querySelector('.image-lightbox__next');
    var closeBtn = lb.querySelector('.image-lightbox__close');

    var gallery = [];
    var currentIndex = 0;

    function showImage() {
        img.src = gallery[currentIndex];
        if (gallery.length > 1) {
            counter.textContent = (currentIndex + 1) + ' / ' + gallery.length;
            lb.classList.remove('image-lightbox--single');
        } else {
            counter.textContent = '';
            lb.classList.add('image-lightbox--single');
        }
    }

    function openLightbox(src, group) {
        if (group) {
            gallery = [];
            var items = document.querySelectorAll('[data-lightbox-group="' + group + '"]');
            items.forEach(function(el) { gallery.push(el.getAttribute('data-lightbox')); });
            currentIndex = gallery.indexOf(src);
            if (currentIndex < 0) currentIndex = 0;
        } else {
            gallery = [src];
            currentIndex = 0;
        }
        showImage();
        lb.classList.add('active');
        document.body.style.overflow = 'hidden';
    }

    function closeLightbox() {
        lb.classList.remove('active');
        document.body.style.overflow = '';
        img.src = '';
    }

    function prev() {
        if (gallery.length <= 1) return;
        currentIndex = (currentIndex - 1 + gallery.length) % gallery.length;
        showImage();
    }

    function next() {
        if (gallery.length <= 1) return;
        currentIndex = (currentIndex + 1) % gallery.length;
        showImage();
    }

    document.addEventListener('click', function(e) {
        var el = e.target.closest('[data-lightbox]');
        if (el) {
            e.preventDefault();
            openLightbox(el.getAttribute('data-lightbox'), el.getAttribute('data-lightbox-group'));
        }
    });

    closeBtn.addEventListener('click', closeLightbox);
    overlay.addEventListener('click', closeLightbox);
    prevBtn.addEventListener('click', prev);
    nextBtn.addEventListener('click', next);

    document.addEventListener('keydown', function(e) {
        if (!lb.classList.contains('active')) return;
        if (e.key === 'Escape') closeLightbox();
        if (e.key === 'ArrowLeft') prev();
        if (e.key === 'ArrowRight') next();
    });
})();
</script>
