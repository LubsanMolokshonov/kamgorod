<?php
/**
 * Гибридный FAQ для посадочных и детальных страниц.
 *
 * Из большого пула вопросов-ответов детерминированно (по seed страницы) выбирается
 * поднабор — так на каждой странице свой набор вопросов, но стабильный между
 * перезагрузками. В тексты подставляются переменные страницы ({category}, {count},
 * {price_min}, {price_max}, {spec}, {product} и т.п.) — FAQ становится уникальным
 * и по составу, и по содержанию.
 *
 * Возвращает формат $faqItems ([['q'=>..,'a'=>..], ...]) для существующих
 * buildFaqJsonLd() / renderFaqList() из includes/faq-helper.php.
 */

require_once __DIR__ . '/seeded-shuffle.php';

/**
 * @param array  $pool   [ ['q'=>'...', 'a'=>'...'], ... ] — вопросы с плейсхолдерами {key}
 * @param string $seed   стабильный ключ страницы (page_key / entityType:id)
 * @param array  $vars   [ 'category'=>'...', 'count'=>12, 'price_min'=>'3 900', ... ]
 * @param int    $count  сколько вопросов вывести
 * @return array         $faqItems с подставленными переменными
 */
function buildLandingFaq(array $pool, string $seed, array $vars = [], int $count = 6): array
{
    if (empty($pool)) {
        return [];
    }

    // Плейсхолдеры вида {key} → значение. Значения экранируем: текст пула — доверенные
    // константы (renderFaqList выводит их сырыми), а вот $vars несут данные из БД
    // (название товара, категория) — их нельзя пускать в вывод без htmlspecialchars.
    $replacements = [];
    foreach ($vars as $k => $v) {
        $v = (string)$v;
        // Пустое значение НЕ подставляем: плейсхолдер останется и вопрос отфильтруется ниже —
        // иначе получили бы огрызок «Стоимость — от  ₽».
        if ($v === '') {
            continue;
        }
        $replacements['{' . $k . '}'] = htmlspecialchars($v, ENT_QUOTES, 'UTF-8');
    }

    $selected = seededShuffle($pool, $seed);

    $out = [];
    foreach ($selected as $item) {
        $q = strtr((string)($item['q'] ?? ''), $replacements);
        $a = strtr((string)($item['a'] ?? ''), $replacements);

        // Вопрос требует переменную, которой на этой странице нет (остался «{...}») —
        // пропускаем, чтобы не показать «Осталось {count} мест».
        if (preg_match('/\{[a-z_]+\}/', $q . $a)) {
            continue;
        }
        $out[] = ['q' => $q, 'a' => $a];
        if (count($out) >= $count) {
            break;
        }
    }
    return $out;
}

